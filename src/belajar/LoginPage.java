    private void login() {
        String nama = nameinput.getText();
        char[] passwordChars = pwinput.getPassword();
        String password = String.valueOf(passwordChars);
        query = "SELECT e.*, r.* FROM employee e INNER JOIN role r on e.role_id = r.id WHERE e.name = ? AND e.password = ?;";
        try (Connection connection = DB.connectdb(); PreparedStatement preparedStatement = connection.prepareStatement(query)) {
            preparedStatement.setString(1, nama);
            preparedStatement.setString(2, password);
            ResultSet resultSet = preparedStatement.executeQuery();
            boolean userFound = false;
            while (resultSet.next()) {
                String name = resultSet.getString("name");
                String role = resultSet.getString("role");

                System.out.println(name);
                System.out.println(role);
                userFound = true;
                switch (role) {
                    case "Admin" -> {
                        dispose();
                        AdminPanel.main((null));
                    }
                    case "Cashier" -> {
                        dispose();
                        CashierPanel.main(null);
                    }
                    default ->
                        JOptionPane.showMessageDialog(null, "Role not found");
                }
            }
            if (!userFound) {
                // No user found with provided credentials
                JOptionPane.showMessageDialog(null, "Incorrect username or password");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }

